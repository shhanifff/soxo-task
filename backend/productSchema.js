import mongoose from "mongoose";

const userScheme = mongoose.Schema({
  title: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  price: {
    type: String,
    required: true,
  },
  image: {
    type: String,
    required: true,
  },
});

export const ProductModel = mongoose.model("products", userScheme);
