import mongoose from "mongoose";

const userScheme=mongoose.Schema({
    name:{
        type:String,
        required:true
    },
    email:{
        type:String,
        required:true
    },
    password:{
        type:String,
        required:true
    },
    role:{
        type:String,
        default:"user"
    }
})

export const userModel=mongoose.model('users',userScheme);
